// Language change function
function changeLanguage(language) {
  const elements = document.querySelectorAll('[data-id]');
  
  elements.forEach(element => {
    const key = element.getAttribute('data-id');
    if (translations[language] && translations[language][key]) {
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = translations[language][key];
      } else if (key === 'mission-list' || key === 'contact-address' || key === 'contact-hours') {
        element.innerHTML = translations[language][key];
      } else if (element.tagName === 'TITLE') {
        element.textContent = translations[language][key];
      } else if (element.tagName === 'META') {
        element.setAttribute('content', translations[language][key]);
      } else {
        element.textContent = translations[language][key];
      }
    }
  });

  // Update page title and meta description
  const title = document.querySelector('title[data-id]');
  const metaDesc = document.querySelector('meta[name="description"][data-id]');
  
  if (title) {
    const titleKey = title.getAttribute('data-id');
    if (translations[language][titleKey]) {
      document.title = translations[language][titleKey];
    }
  }
  
  if (metaDesc) {
    const descKey = metaDesc.getAttribute('data-id');
    if (translations[language][descKey]) {
      metaDesc.setAttribute('content', translations[language][descKey]);
    }
  }

  // Update html lang attribute
  document.documentElement.lang = language;
}